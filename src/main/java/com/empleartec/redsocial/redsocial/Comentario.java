package com.empleartec.redsocial.redsocial;

public class Comentario {
	
	//	ATRIBUTOS
	
	private long idUser;
	private String texto;
	
	//	GETTERS Y SETTERS
	
	public long getIdUser() {
		return idUser;
	}
	public void setIdUser(long idUser) {
		this.idUser = idUser;
	}
	public String getTexto() {
		return texto;
	}
	public void setTexto(String texto) {
		this.texto = texto;
	}
	

}
